# structural_duplication.py

def compute_sum(x, y):
    result = x + y
    return result

def compute_product(x, y):
    result = x * y
    return result

# Same logic, different names
def sum_values(a, b):
    total = a + b
    return total

def multiply_values(a, b):
    product = a * b
    return product
